#!/usr/bin/env node
/**
 * migrate-env-to-root.mjs — one-time merger.
 *
 * Reads every existing per-project .env / .env.local file, renames keys
 * per env.config.json (project-prefixing where the same name is used across
 * multiple projects), and APPENDS to <repo-root>/.env. Backs up each source
 * file to <source>.backup-YYYYMMDD.
 *
 * Run once after upgrading to the centralized scheme. Idempotent: if root
 * .env already has a key, this script will not overwrite it — values
 * already at root win.
 *
 * Values are NEVER printed to stdout — only key names with set/empty status.
 *
 * USAGE
 *   pnpm migrate:env                # do it
 *   pnpm migrate:env --dry-run      # show what would happen
 */

import { readFileSync, writeFileSync, appendFileSync, existsSync, renameSync, copyFileSync } from 'node:fs';
import { dirname, resolve, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, '..');
const ROOT_ENV = join(REPO_ROOT, '.env');
const CONFIG_PATH = join(REPO_ROOT, 'env.config.json');

const DRY = process.argv.includes('--dry-run');

function parseEnv(text) {
  const out = new Map();
  for (const rawLine of text.split(/\r?\n/)) {
    const line = rawLine.trim();
    if (!line || line.startsWith('#')) continue;
    const eq = line.indexOf('=');
    if (eq === -1) continue;
    const key = line.slice(0, eq).trim();
    let val = line.slice(eq + 1);
    if (val.length >= 2 && val.startsWith('"') && val.endsWith('"')) val = val.slice(1, -1);
    out.set(key, val);
  }
  return out;
}

/** Inverse of env.config.json: given a project + local key, return the
 *  root-level key it maps to. Used when reading an existing .env.local and
 *  re-emitting under the root namespace.
 */
function buildInverseMap(config) {
  const inverse = new Map(); // projectName -> Map<localKey, rootKey>
  const shared = new Set(config.shared ?? []);

  for (const [projectName, cfg] of Object.entries(config.projects)) {
    const localToRoot = new Map();
    for (const k of shared) localToRoot.set(k, k);
    for (const entry of cfg.include) {
      if (typeof entry === 'string') {
        localToRoot.set(entry, entry);
      } else if (entry.from && entry.to) {
        localToRoot.set(entry.to, entry.from);
      }
    }
    inverse.set(projectName, localToRoot);
  }
  return inverse;
}

function findExistingEnvFiles(config) {
  const candidates = [];
  for (const projectName of Object.keys(config.projects)) {
    for (const name of ['.env.local', '.env']) {
      const p = join(REPO_ROOT, projectName, name);
      if (existsSync(p)) candidates.push({ projectName, path: p });
    }
  }
  return candidates;
}

function todayStamp() {
  const d = new Date();
  return `${d.getUTCFullYear()}${String(d.getUTCMonth() + 1).padStart(2, '0')}${String(d.getUTCDate()).padStart(2, '0')}`;
}

function main() {
  if (!existsSync(CONFIG_PATH)) {
    console.error('✗ env.config.json missing');
    process.exit(2);
  }
  const config = JSON.parse(readFileSync(CONFIG_PATH, 'utf-8'));
  const inverse = buildInverseMap(config);
  const sources = findExistingEnvFiles(config);

  if (!existsSync(ROOT_ENV)) {
    if (DRY) {
      console.log('would create empty root .env');
    } else {
      writeFileSync(ROOT_ENV, '# Generated by migrate-env-to-root.mjs\n# Edit values here. Run `pnpm sync:env` after.\n\n');
    }
  }

  const rootCurrent = parseEnv(readFileSync(ROOT_ENV, 'utf-8'));
  const toAppend = []; // [rootKey, value, sourceProject]
  const conflicts = []; // [rootKey, existingFlag, sourceProject]

  for (const src of sources) {
    const projectName = src.projectName;
    const localToRoot = inverse.get(projectName);
    if (!localToRoot) {
      console.warn(`  skip: ${projectName} (no mapping in env.config.json)`);
      continue;
    }
    const content = readFileSync(src.path, 'utf-8');
    const parsed = parseEnv(content);
    for (const [localKey, value] of parsed.entries()) {
      const rootKey = localToRoot.get(localKey);
      if (!rootKey) {
        // Local key not in mapping — preserve as project-prefixed root key
        const fallback = `${projectName.toUpperCase().replace(/[^A-Z0-9]+/g, '_')}_${localKey}`;
        if (rootCurrent.has(fallback)) {
          conflicts.push([fallback, !!rootCurrent.get(fallback), projectName]);
          continue;
        }
        toAppend.push([fallback, value, projectName, 'unmapped']);
        rootCurrent.set(fallback, value);
        continue;
      }
      if (rootCurrent.has(rootKey)) {
        // Root already has it. Conflict if both are non-empty and differ.
        const existing = rootCurrent.get(rootKey) ?? '';
        if (existing.length > 0 && existing !== value && value.length > 0) {
          conflicts.push([rootKey, true, projectName]);
        }
        continue;
      }
      toAppend.push([rootKey, value, projectName, 'mapped']);
      rootCurrent.set(rootKey, value);
    }
  }

  console.log('Sources scanned:');
  for (const s of sources) console.log(`  ${s.path}`);
  console.log('');

  console.log(`Keys to append to root .env: ${toAppend.length}`);
  for (const [k, v, src, kind] of toAppend) {
    const status = v.length > 0 ? `<set len=${v.length}>` : '<empty>';
    console.log(`  + ${k}=${status}  (from ${src}${kind === 'unmapped' ? ', UNMAPPED → auto-prefixed' : ''})`);
  }
  console.log('');

  if (conflicts.length > 0) {
    console.log(`CONFLICTS (root already has a different non-empty value): ${conflicts.length}`);
    for (const [k, _flag, src] of conflicts) {
      console.log(`  ⚠ ${k}  — root wins; ${src} value ignored`);
    }
    console.log('');
  }

  if (DRY) {
    console.log('(dry-run; no files changed)');
    return;
  }

  // Append to root .env
  if (toAppend.length > 0) {
    let block = `\n# Auto-merged ${new Date().toISOString().slice(0, 10)} by migrate-env-to-root.mjs\n`;
    for (const [k, v] of toAppend) block += `${k}=${v}\n`;
    appendFileSync(ROOT_ENV, block);
    console.log(`✓ appended ${toAppend.length} keys to ${ROOT_ENV}`);
  }

  // Back up source files
  const stamp = todayStamp();
  for (const s of sources) {
    const backup = `${s.path}.backup-${stamp}`;
    copyFileSync(s.path, backup);
    console.log(`✓ backed up: ${s.path} → ${backup}`);
  }

  console.log('\nNext steps:');
  console.log('  1. Verify <repo-root>/.env values look right.');
  console.log('  2. Run `pnpm sync:env` to regenerate per-project .env.local files.');
  console.log('  3. After confirming each project still runs, delete the .backup-* files.');
  console.log('  4. (Optional) delete the originals once .env.local is generated — sync:env will overwrite them anyway.');
}

main();
