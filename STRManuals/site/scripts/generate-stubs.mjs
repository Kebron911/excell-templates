async function buildPdf({ sku, title, chapters }) {
  const doc = await PDFDocument.create();
  const garamond = await doc.embedFont(StandardFonts.TimesRoman);
  const garamondBold = await doc.embedFont(StandardFonts.TimesRomanBold);
  const inter = await doc.embedFont(StandardFonts.Helvetica);
  const interBold = await doc.embedFont(StandardFonts.HelveticaBold);
  const mono = await doc.embedFont(StandardFonts.Courier);

  const ink = rgb(0.10, 0.086, 0.07);
  const accent = rgb(0.48, 0.23, 0.18);
  const mute = rgb(0.42, 0.39, 0.35);

  // === Cover page ===
  const cover = doc.addPage([612, 792]);
  cover.drawRectangle({ x: 40, y: 40, width: 532, height: 712, borderColor: ink, borderWidth: 1 });

  cover.drawText(sku, {
    x: 612 / 2 - inter.widthOfTextAtSize(sku, 14) / 2,
    y: 660,
    size: 14,
    font: interBold,
    color: accent,
  });
  cover.drawLine({ start: { x: 220, y: 640 }, end: { x: 392, y: 640 }, thickness: 0.5, color: ink });

  // Title — wrap at 18 chars
  const words = title.split(' ');
  const lines = [];
  let line = '';
  for (const w of words) {
    if ((line + ' ' + w).trim().length > 18) {
      lines.push(line.trim());
      line = w;
    } else {
      line = (line + ' ' + w).trim();
    }
  }
  if (line) lines.push(line);

  let y = 480;
  for (const l of lines) {
    cover.drawText(l, {
      x: 612 / 2 - garamondBold.widthOfTextAtSize(l, 38) / 2,
      y,
      size: 38,
      font: garamondBold,
      color: ink,
    });
    y -= 50;
  }

  cover.drawLine({ start: { x: 220, y: 240 }, end: { x: 392, y: 240 }, thickness: 0.5, color: ink });
  cover.drawText('STR Manuals', {
    x: 612 / 2 - inter.widthOfTextAtSize('STR Manuals', 11) / 2,
    y: 90,
    size: 11,
    font: inter,
    color: mute,
  });
  cover.drawText('· v1 ·', {
    x: 612 / 2 - inter.widthOfTextAtSize('· v1 ·', 11) / 2,
    y: 70,
    size: 11,
    font: inter,
    color: mute,
  });

  // === Stub note page ===
  const note = doc.addPage([612, 792]);
  note.drawText('— Development stub —', {
    x: 60, y: 720, size: 11, font: inter, color: accent,
  });
  note.drawText('This PDF is a placeholder generated by scripts/generate-stubs.mjs.', {
    x: 60, y: 695, size: 11, font: inter, color: mute,
  });
  note.drawText('Real manuscripts ship via SFTP to Hostinger and overwrite this file.', {
    x: 60, y: 680, size: 11, font: inter, color: mute,
  });

  note.drawText('Outline', {
    x: 60, y: 620, size: 24, font: garamondBold, color: ink,
  });
  note.drawLine({ start: { x: 60, y: 605 }, end: { x: 552, y: 605 }, thickness: 0.5, color: ink });

  let cy = 565;
  chapters.forEach((c, i) => {
    note.drawText(String(i + 1).padStart(2, '0'), {
      x: 60, y: cy, size: 10, font: mono, color: mute,
    });
    note.drawText(c, {
      x: 100, y: cy, size: 13, font: inter, color: ink,
    });
    cy -= 28;
  });

  // === Chapter pages (one per chapter, brief stub text) ===
  for (let i = 0; i < chapters.length; i++) {
    const p = doc.addPage([612, 792]);
    const ch = chapters[i];
    p.drawText(`Chapter ${i + 1}`, {
      x: 60, y: 720, size: 11, font: inter, color: accent,
    });
    p.drawText(ch, {
      x: 60, y: 680, size: 24, font: garamondBold, color: ink,
    });
    p.drawLine({ start: { x: 60, y: 665 }, end: { x: 552, y: 665 }, thickness: 0.5, color: ink });
    p.drawText('Real chapter content goes here.', {
      x: 60, y: 620, size: 12, font: inter, color: mute,
    });
    p.drawText('When the manuscript is authored, this stub is replaced and the file', {
      x: 60, y: 600, size: 11, font: inter, color: mute,
    });
    p.drawText('is uploaded to /private/manuals/<sku>/v1.pdf via SFTP.', {
      x: 60, y: 585, size: 11, font: inter, color: mute,
    });
  }

  return doc.save();
}

async function main() {
  for (const stub of STUBS) {
    const fullPath = resolve(process.cwd(), stub.path);
    await mkdir(dirname(fullPath), { recursive: true });
    const bytes = await buildPdf(stub);
    await writeFile(fullPath, bytes);
    console.log(`✓ ${stub.path}  (${stub.sku}, ${(bytes.length / 1024).toFixed(1)} KB)`);
  }
  console.log(`\nGenerated ${STUBS.length} stub PDFs.`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
